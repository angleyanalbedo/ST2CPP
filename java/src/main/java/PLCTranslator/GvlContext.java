    /**
     * 生成 gvl_layout.gen.h — 仅用于 reinterpret_cast 做直接成员访问
     */
    public String emitGVLLayoutHeader() {
        StringBuilder sb = new StringBuilder();
        sb.append("// Auto-generated GVL layout struct — for direct member access\n");
        sb.append("// DO NOT EDIT — generated by ST2C++ compiler\n");
        sb.append("#pragma once\n");
        sb.append("#include \"core/gvl.h\"\n\n");
        sb.append("namespace rt_plc {\n\n");
        sb.append("struct GVL_LAYOUT {\n");
        for (String varName : offsetMap.keySet()) {
            String type = typeMap.get(varName);
            if (type == null) continue;
            if (ioVarMap.containsKey(varName)) continue;
            if (type.startsWith("ARRAY[")) {
                String elemType = arrayElemTypeMap.get(varName);
                int[][] bounds = arrayBoundsMap.get(varName);
                int total = (bounds != null) ? bounds[0][2] : 0;
                sb.append("    ").append(elemType).append(" ")
                  .append(getMangledName(varName)).append("[").append(total).append("];\n");
            } else {
                sb.append("    ").append(type).append(" ")
                  .append(getMangledName(varName)).append(";\n");
            }
        }
        sb.append("};\n\n");
        sb.append("inline GVL_LAYOUT& gvl_layout(GVL& gvl) {\n");
        sb.append("    return *reinterpret_cast<GVL_LAYOUT*>(gvl.memory);\n");
        sb.append("}\n\n");
        sb.append("} // namespace rt_plc\n");
        return sb.toString();
    }

